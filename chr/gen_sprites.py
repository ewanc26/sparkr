"""
chr/gen_sprites.py  —  Sparky sprite sheet generator for Sparkr NES
Produces chr/sprites.chr  (4096 bytes, one 4K NES sprite pattern table).

Run from the project root:
    python3 chr/gen_sprites.py

Tile layout (256-tile bank, 16-wide grid):
  Row 0, cols 0-1   → idle   TL/TR   ($00/$01)
  Row 0, cols 8-9   → idle   BL/BR   ($08/$09)
  Row 0, cols 2-3   → run-0  TL/TR   ($02/$03)
  Row 0, cols 10-11 → run-0  BL/BR   ($0A/$0B)
  Row 0, cols 4-5   → run-1  TL/TR   ($04/$05)
  Row 0, cols 12-13 → run-1  BL/BR   ($0C/$0D)
  Row 0, cols 6-7   → jump   TL/TR   ($06/$07)
  Row 0, cols 14-15 → jump   BL/BR   ($0E/$0F)
  Row 1, cols 0-1   → dash   TL/TR   ($10/$11)
  Row 1, cols 8-9   → dash   BL/BR   ($18/$19)
  Row 1, cols 2-3   → attack TL/TR   ($12/$13)
  Row 1, cols 10-11 → attack BL/BR   ($1A/$1B)

  Row 2, col 0      → Zapper frame 0          ($20)
  Row 2, col 1      → Zapper frame 1          ($21)
  Row 2, col 2      → Floater frame 0         ($22)
  Row 2, col 3      → Floater frame 1         ($23)
  Row 2, col 4      → Shocker frame 0         ($24)
  Row 2, col 5      → Shocker frame 1         ($25)
  Row 2, col 6      → Spark projectile        ($26)
  Row 2, col 7      → Heart (HUD, 1 HP dot)   ($27)

Colour key used in frame arrays:
  0 = transparent
  1 = dark outline  (NES palette index 1 in sprite palettes sp0/sp1)
  2 = body colour   (NES palette index 2)
  3 = bright spark  (NES palette index 3)

Suggested NES palettes in main.s for Sparky:
  sp0 (dim mode):       $BG, $16, $28, $38   (rust → golden → bright yellow)
  sp1 (overload mode):  $BG, $02, $21, $30   (dark blue → cyan → white)
"""

