# ── helpers ─────────────────────────────────────────────────────────────────

def hflip(frame):
    return [row[::-1] for row in frame]

def tile_to_chr(pixels_8x8):
    """Encode an 8×8 list-of-lists (colour 0-3) to 16 NES CHR bytes."""
    bp0, bp1 = [], []
    for row in pixels_8x8:
        b0 = b1 = 0
        for x, c in enumerate(row):
            bit = 7 - x
            b0 |= (c & 1) << bit
            b1 |= ((c >> 1) & 1) << bit
        bp0.append(b0)
        bp1.append(b1)
    return bytes(bp0 + bp1)

def frame_to_tiles(f):
    """Split a 16×16 frame into (TL, TR, BL, BR) CHR byte strings."""
    tl = tile_to_chr([r[0:8] for r in f[0:8]])
    tr = tile_to_chr([r[8:16] for r in f[0:8]])
    bl = tile_to_chr([r[0:8] for r in f[8:16]])
    br = tile_to_chr([r[8:16] for r in f[8:16]])
    return tl, tr, bl, br

def stamp(buf, tile_idx, data16):
    off = tile_idx * 16
    buf[off:off + 16] = data16

