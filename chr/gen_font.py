#!/usr/bin/env python3
"""
chr/gen_font.py  —  Patch pixel-art letter glyphs into background.chr.

Run once (or any time background.xcf is regenerated):
    python3 chr/gen_font.py

Tile slot assignments (all were empty in the original CHR):
  $47 A   $48 D   $49 E   $4A G   $4B K
  $4C M   $4D O   $4E P   $4F R
  $5A S   $5B T   $5C U   $5D V

Each glyph uses NES colour 2 only (plane0 = 0, plane1 = pixel_mask) so
the text appears in bg-palette-0 colour-2, which screens.s sets per screen:
  Title screen  : colour 2 = $30 (white)
  Game-over     : colour 2 = $36 (red-orange)
  Pause overlay : colour 2 = world bg-palette colour-2 (light green / teal …)
"""

import pathlib, sys

CHR_FILE = pathlib.Path(__file__).parent / "background.chr"

# 8×8 bitmaps — one byte per row, MSB = leftmost pixel, 1 = lit pixel.
FONT = {
    0x47: [0x3C,0x66,0xC3,0xFF,0xC3,0xC3,0xC3,0x00],  # A
    0x48: [0xFC,0xC6,0xC3,0xC3,0xC3,0xC6,0xFC,0x00],  # D
    0x49: [0xFE,0xC0,0xC0,0xFC,0xC0,0xC0,0xFE,0x00],  # E
    0x4A: [0x3C,0x66,0xC0,0xCF,0xC3,0x66,0x3C,0x00],  # G
    0x4B: [0xC6,0xCC,0xD8,0xF0,0xD8,0xCC,0xC6,0x00],  # K
    0x4C: [0xC3,0xE7,0xFF,0xDB,0xC3,0xC3,0xC3,0x00],  # M
    0x4D: [0x3C,0x66,0xC3,0xC3,0xC3,0x66,0x3C,0x00],  # O
    0x4E: [0xFC,0xC6,0xC6,0xFC,0xC0,0xC0,0xC0,0x00],  # P
    0x4F: [0xFC,0xC6,0xC6,0xFC,0xD8,0xCC,0xC6,0x00],  # R
    0x5A: [0x7E,0xC0,0xC0,0x7C,0x03,0x03,0x7E,0x00],  # S
    0x5B: [0xFE,0x18,0x18,0x18,0x18,0x18,0x18,0x00],  # T
    0x5C: [0xC3,0xC3,0xC3,0xC3,0xC3,0x66,0x3C,0x00],  # U
    0x5D: [0xC3,0xC3,0xC3,0x66,0x66,0x3C,0x18,0x00],  # V
}


def encode_tile(rows):
    """Return 16 CHR bytes for a glyph using colour 2 (plane0=0, plane1=mask)."""
    return bytes(8) + bytes(rows)


def main():
    if not CHR_FILE.exists():
        print(f"ERROR: {CHR_FILE} not found — run 'make' first to generate it.")
        sys.exit(1)

    data = bytearray(CHR_FILE.read_bytes())

    for tile_idx, rows in sorted(FONT.items()):
        off = tile_idx * 16
        data[off:off + 16] = encode_tile(rows)
        print(f"  patched tile ${tile_idx:02X}")

    CHR_FILE.write_bytes(data)
    print(f"Done — {len(FONT)} glyphs written to {CHR_FILE}")


if __name__ == "__main__":
    main()
